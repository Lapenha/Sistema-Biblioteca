    private void consultarUsuarioButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarUsuarioButtonActionPerformed
        String nome = nomeTextField.getText();
        String senha = senhaTextField.getText();
        Usuario usuario = new Usuario(nome, senha);
         try{
             if (UsuarioDAO.consultaUsuario(usuario)){
                 tipoUsuarioComboBox.setSelectedItem(usuario.getTipo());
                        if(usuario.getTipo().equals("comum")){
                            preferenciaLivro1ComboBox.setSelectedItem(usuario.getPreferencia_livro_1());
                            preferenciaLivro2ComboBox.setSelectedItem(usuario.getPreferencia_livro_2());
                        }else {
                            preferenciaLivro1ComboBox.setSelectedIndex(0);
                            preferenciaLivro2ComboBox.setSelectedIndex(0);
                        }
                        
             } else {
                  JOptionPane.showMessageDialog(null, "Usuario não cadastrado", "Erro", JOptionPane.WARNING_MESSAGE);
             }
         }catch(Exception e){
             JOptionPane.showMessageDialog(null, "Problemas técnicos", "Erro", JOptionPane.WARNING_MESSAGE);
         }
    }//GEN-LAST:event_consultarUsuarioButtonActionPerformed

    private void exibirUsuariosButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exibirUsuariosButtonActionPerformed
        // TODO add your handling code here:
        try{
            List<Usuario> usuarios = UsuarioDAO.exibirUsuarios();
            
            DefaultTableModel model= (DefaultTableModel) usuariosTable.getModel();
            model.setRowCount(0);
            
            for (Usuario u: usuarios){
                model.addRow(new Object[] {
                    u.getNome(),
                    u.getTipo(),
                    u.getSenha(),
                    u.getPreferencia_livro_1(),
                    u.getPreferencia_livro_2()
                });
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(this, "Erro ao exibir usuarios: "  + ex.getMessage());
        }
        
        
    }//GEN-LAST:event_exibirUsuariosButtonActionPerformed

