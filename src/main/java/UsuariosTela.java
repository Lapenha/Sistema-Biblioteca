    private void cadastrarUsuarioButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarUsuarioButtonActionPerformed
            String nome = nomeTextField.getText();
            String senha = senhaTextField.getText();
            String tipoDeUsuario = tipoUsuarioComboBox.getItemAt(tipoUsuarioComboBox.getSelectedIndex());
            String preferenciaLivro1 = preferenciaLivro1ComboBox.getItemAt(preferenciaLivro1ComboBox.getSelectedIndex());
            String preferenciaLivro2 = preferenciaLivro2ComboBox.getItemAt(preferenciaLivro2ComboBox.getSelectedIndex());
            Usuario usuario = new Usuario(nome, senha, tipoDeUsuario, preferenciaLivro1, preferenciaLivro2);
           try{
               UsuarioDAO.cadastraUsuario(usuario);
               JOptionPane.showMessageDialog(null, "Usuario " + nome + " cadastrado com sucesso!");
           }catch(SQLException e){
               e.printStackTrace();
           }
            
    }//GEN-LAST:event_cadastrarUsuarioButtonActionPerformed

    private void removerUsuarioButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerUsuarioButtonActionPerformed
         String nome = nomeTextField.getText();
    Usuario usuario = new Usuario(nome);

    try {
        UsuarioDAO.removeUsuario(usuario);
        JOptionPane.showMessageDialog(null, "Usuário '" + nome + "' removido com sucesso!");
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Erro ao remover o usuário: " + e.getMessage());
    }
    }//GEN-LAST:event_removerUsuarioButtonActionPerformed

